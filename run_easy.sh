#!/bin/bash

# ========================================================
# 기능: Clean-Disk 간편 실행 런처 (담당: 최영현)
# 역할: 복잡한 명령어 없이 바로 실행 가능하도록 함
# ========================================================

# 현재 스크립트가 있는 위치로 이동 (경로 문제 해결)
cd "$(dirname "$0")"

echo "========================================"
echo "  Clean-My-Disk 실행 도우미"
echo "========================================"
echo "1.   초기 설정 (Setup)"
echo "2.  디스크 정리 시작 (Start)"
echo "3.  종료 (Exit)"
echo "----------------------------------------"
read -p "원하는 기능을 선택하세요 (1-3): " choice

case $choice in
    1)
        echo "초기 설정을 시작합니다..."
        # 파트너가 만든 setup 기능 호출
        ./scripts/clean-disk.sh setup
        ;;
    2)
        echo "프로그램을 실행합니다..."
        # 메인 프로그램 호출
        ./scripts/clean-disk.sh
        ;;
    3)
        echo "종료합니다."
        exit 0
        ;;
    *)
        echo "잘못된 입력입니다."
        ;;
esac

# 실행이 너무 빨리 꺼지는 것을 방지
echo ""
read -p "엔터 키를 누르면 종료됩니다..."